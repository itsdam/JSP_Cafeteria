package cafeteria;

public class Cafeteria {
	private int cafeteriaID;
	private String cafeteriaTitle;
	private String memberID;
	private String cafeteriaDate;
	private String cafeteriaContent;
	
	public int getCafeteriaID() {
		return cafeteriaID;
	}
	public void setCafeteriaID(int cafeteriaID) {
		this.cafeteriaID = cafeteriaID;
	}
	public String getCafeteriaTitle() {
		return cafeteriaTitle;
	}
	public void setCafeteriaTitle(String cafeteriaTitle) {
		this.cafeteriaTitle = cafeteriaTitle;
	}
	public String getMemberID() {
		return memberID;
	}
	public void setMemberID(String memberID) {
		this.memberID = memberID;
	}
	public String getCafeteriaDate() {
		return cafeteriaDate;
	}
	public void setCafeteriaDate(String cafeteriaDate) {
		this.cafeteriaDate = cafeteriaDate;
	}
	public String getCafeteriaContent() {
		return cafeteriaContent;
	}
	public void setCafeteriaContent(String cafeteriaContent) {
		this.cafeteriaContent = cafeteriaContent;
	}

}
